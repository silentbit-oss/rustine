static enum cmd_retval cmd_refresh_client_exec(struct cmd *self, struct cmdq_item *item)
{
  struct args *args = self->args;
  struct client *c;
  unsigned int c_idx = 0;
  const char *size;
  unsigned int size_idx = 0;
  u_int w;
  u_int h;
  if ((c_idx = cmd_find_client(item, args_get(args, 't'), 0)) == 0)
  {
    return CMD_RETURN_ERROR;
  }
  if (args_has(args, 'C'))
  {
    if ((size_idx = args_get(args, 'C')) == 0)
    {
      cmdq_error(item, "missing size");
      return CMD_RETURN_ERROR;
    }
    if (sscanf(size, "%u,%u", &w, &h) != 2)
    {
      cmdq_error(item, "bad size argument");
      return CMD_RETURN_ERROR;
    }
    if ((((w < 2) || (w > 5000)) || (h < 2)) || (h > 5000))
    {
      cmdq_error(item, "size too small or too big");
      return CMD_RETURN_ERROR;
    }
    if (!(c->flags & 0x2000))
    {
      cmdq_error(item, "not a control client");
      return CMD_RETURN_ERROR;
    }
    tty_set_size(&c->tty, w, h);
    c->flags |= 0x400000;
    recalculate_sizes();
  }
  else
    if (args_has(args, 'S'))
  {
    c->flags |= 0x80000;
    server_status_client(c);
  }
  else
  {
    c->flags |= 0x80000;
    server_redraw_client(c);
  }
  return CMD_RETURN_NORMAL;
}


