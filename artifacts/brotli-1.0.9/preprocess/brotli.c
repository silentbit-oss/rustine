static int ParseInt(const char* s, int low, int high, int* result) {
  int value = 0;
  int i;
  for (i = 0; i < 5; ++i) {
    char c = s[i];
    if (c == 0) break;
    if (s[i] < '0' || s[i] > '9') return 0;
    value = (10 * value) + (c - '0');
  }
  if (i == 0) return 0;
  if (i > 1 && s[0] == '0') return 0;
  if (s[i] != 0) return 0;
  if (value < low || value > high) return 0;
  *result = value;
  return 1;
}


